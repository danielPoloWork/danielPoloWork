#!/usr/bin/env node
/**
 * sync-portfolio.mjs
 *
 * Regenerates the auto-managed Portfolio block in README.md from the
 * `.portfolio.json` metadata file shipped inside each PUBLIC repository.
 *
 * Source of truth: each repo's `.portfolio.json`
 *   { "title": string, "tags": string[], "order": number,
 *     "description": { "en": string, "ja"?: string, "zh"?: string } }
 *
 * The script only ever rewrites the text BETWEEN these markers in README.md:
 *   <!-- PORTFOLIO:START ... -->
 *   <!-- PORTFOLIO:END -->
 * Everything outside the markers is left untouched.
 *
 * Usage:
 *   node scripts/sync-portfolio.mjs          # write changes
 *   node scripts/sync-portfolio.mjs --check  # exit 1 if README is out of date (CI-friendly)
 *
 * Requirements: GitHub CLI (`gh`) authenticated as the profile owner.
 */

import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const OWNER = "danielPoloWork";
const LANG = "en"; // README is English; switch here to localise the block
const START = "<!-- PORTFOLIO:START — generated by scripts/sync-portfolio.mjs · do not edit by hand -->";
const END = "<!-- PORTFOLIO:END -->";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const README = join(ROOT, "README.md");

function gh(args, { quiet = false } = {}) {
  return execFileSync("gh", args, {
    encoding: "utf8",
    maxBuffer: 32 * 1024 * 1024,
    stdio: ["ignore", "pipe", quiet ? "ignore" : "inherit"],
  });
}

function listPublicRepos() {
  const raw = gh([
    "repo", "list", OWNER,
    "--no-archived",
    "--limit", "200",
    "--json", "name,visibility,isFork,primaryLanguage,url",
  ]);
  return JSON.parse(raw)
    .filter((r) => r.visibility === "PUBLIC" && !r.isFork && r.name !== OWNER);
}

function fetchPortfolio(name) {
  try {
    const b64 = gh(["api", `repos/${OWNER}/${name}/contents/.portfolio.json`, "--jq", ".content"], { quiet: true });
    return JSON.parse(Buffer.from(b64, "base64").toString("utf8"));
  } catch {
    return null; // no .portfolio.json -> repo opts out of the portfolio
  }
}

function chip(text) {
  return "`" + text + "`";
}

function renderEntry({ repo, meta }) {
  const lang = repo.primaryLanguage?.name;
  const chips = [lang, ...(meta.tags ?? [])].filter(Boolean).map(chip).join(" ");
  const desc =
    (meta.description && (meta.description[LANG] || meta.description.en)) ||
    "";
  return [
    `#### [${meta.title}](${repo.url})`,
    chips,
    "",
    desc,
  ].join("\n");
}

function buildBlock(entries) {
  if (entries.length === 0) {
    return "_No public portfolio repositories found. Add a `.portfolio.json` to a public repo and re-run the sync._";
  }
  return entries.map(renderEntry).join("\n\n");
}

function main() {
  const check = process.argv.includes("--check");

  const repos = listPublicRepos();
  const entries = repos
    .map((repo) => ({ repo, meta: fetchPortfolio(repo.name) }))
    .filter((e) => e.meta !== null)
    .sort((a, b) => {
      const oa = a.meta.order ?? 999;
      const ob = b.meta.order ?? 999;
      if (oa !== ob) return oa - ob;
      return String(a.meta.title).localeCompare(String(b.meta.title));
    });

  const block = buildBlock(entries);

  const readme = readFileSync(README, "utf8");
  const si = readme.indexOf(START);
  const ei = readme.indexOf(END);
  if (si === -1 || ei === -1 || ei < si) {
    console.error(
      `ERROR: markers not found in README.md.\nAdd these two lines where the portfolio list should go:\n  ${START}\n  ${END}`
    );
    process.exit(2);
  }

  const next =
    readme.slice(0, si + START.length) +
    "\n\n" + block + "\n\n" +
    readme.slice(ei);

  if (next === readme) {
    console.log(`Portfolio up to date (${entries.length} public project(s)).`);
    return;
  }

  if (check) {
    console.error("README.md portfolio block is OUT OF DATE. Run: node scripts/sync-portfolio.mjs");
    process.exit(1);
  }

  writeFileSync(README, next);
  console.log(`README.md portfolio block updated (${entries.length} public project(s)):`);
  for (const e of entries) console.log(`  - [${e.meta.order ?? "?"}] ${e.meta.title} (${e.repo.name})`);
}

main();
